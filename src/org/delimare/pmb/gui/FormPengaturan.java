    private void loadData() {
        try {
            ArrayList<Pengaturan> settings = manager.getSemua();
            
            for (Pengaturan p : settings) {
                if (p.getKunci().equals("tanggal_mulai_ujian")) {
                    Date date = format.parse(p.getNilai());
                    calendarMulaiUjian.setDate(date);
                    timeMulai.setTime(date);
                }
                
                if (p.getKunci().equals("durasi_ujian")) {
                    txtDurasiUjian.setText(p.getNilai());
                }
                
                if (p.getKunci().equals("durasi_jeda")) {
                    txtDurasiJeda.setText(p.getNilai());
                }
            }
        } catch (Exception e) {
            Logger.error(this, e.getMessage());
        }
    }

